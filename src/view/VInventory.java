    private void txtSearchIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchIDActionPerformed
        String id = txtSearchID.getText().trim(); // Get the ID from the search text field

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int productId = Integer.parseInt(id); // Parse the product ID

            MInventory minv = new MInventory(); // Use MInventory model for product-related operations
            ResultSet rs = minv.searchProductById(productId); // Call the model's method to search by product ID

            if (rs.next()) {
                // If the product is found, set the corresponding text fields
                txtname.setText(rs.getString("ProductName"));
                txtprice.setText(rs.getString("UnitPrice"));

                // Disable the add button to prevent adding the same product again
                btnadd.setEnabled(false);
            } else {
                // If no product is found with the entered ID
                JOptionPane.showMessageDialog(this, "Product not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // Handle invalid product ID input
            JOptionPane.showMessageDialog(this, "Invalid product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            // Handle SQL exceptions
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSearchIDActionPerformed

