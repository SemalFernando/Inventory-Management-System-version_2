    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        String id = txtSearchID.getText().trim();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            int productId = Integer.parseInt(id);
            Statement st = DBConnection.createDBConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Product WHERE ProductID = " + productId);

            if (rs.next()) {
                String productName = txtname.getText();
                String unitPriceStr = txtprice.getText();

                // Check if any required fields are empty
                if (productName.isEmpty() || unitPriceStr.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "All fields must be filled", "Validation Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Parse unit price
                double unitPrice;
                try {
                    unitPrice = Double.parseDouble(unitPriceStr);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Unit Price must be a valid number", "Validation Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                CInventory cinv = new CInventory();
                cinv.updateProduct(productId, productName, unitPrice);

                clearFields();
                txtname.requestFocus();
                loadProductData();
                btnadd.setEnabled(true);

                JOptionPane.showMessageDialog(this, "Product updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Product not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        String id = txtSearchID.getText().trim();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int productId = Integer.parseInt(id);
            Statement st = DBConnection.createDBConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Product WHERE ProductID = " + productId);

            if (rs.next()) {
                MInventory minv = new MInventory();
                minv.deleteProduct(productId);

                clearFields();
                loadProductData();
                btnadd.setEnabled(true);

                JOptionPane.showMessageDialog(this, "Product deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Product not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid product ID", "Validation Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

